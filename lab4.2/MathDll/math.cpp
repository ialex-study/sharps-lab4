#include "pch.h" // use stdafx.h in Visual Studio 2017 and earlier
#include <utility>
#include <limits.h>
#include "math.h"

double Add(double one, double two) {
	return one + two;
}
double Substract(double one, double two) {
	return one - two;
}
double Compose(double one, double two) {
	return one * two;
}
double Divide(double one, double two) {
	return one / two;
}